#include "Font.h"

//==========================================================================
// Font constructor
//==========================================================================
Mach::Font::Font()
{
	m_IsValid = false;
}

//==========================================================================
// Font constructor
//==========================================================================
Mach::Font::Font(std::string path)
{
	if (!m_Font.loadFromFile(path))
	{
		m_IsValid = false;
	}
	else
	{
		m_IsValid = true;
	}
}

//==========================================================================
// Get the underlying sf::Font
//==========================================================================
sf::Font* Mach::Font::get()
{
	return &m_Font;
}

//==========================================================================
// Query whether or not the Font object contains a valid font
//==========================================================================
bool Mach::Font::isValid()
{
	return m_IsValid;
}
